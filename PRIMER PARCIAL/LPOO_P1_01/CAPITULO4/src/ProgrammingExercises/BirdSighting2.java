public class BirdSighting2 {
    private String name;
    private Integer number;
    private Integer day;

    public BirdSighting2(String name, Integer number, Integer day) {
        this.name = name;
        this.number = number;
        this.day = day;
    }

    public String getName() {
        return name;
    }

    public Integer getNumber() {
        return number;
    }

    public Integer getDay() {
        return day;
    }
}
