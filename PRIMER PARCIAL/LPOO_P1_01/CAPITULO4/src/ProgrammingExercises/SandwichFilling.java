public class SandwichFilling {
    private String type;
    private Integer calories;

    public SandwichFilling(String type, Integer calories) {
        this.type = type;
        this.calories = calories;
    }

    public String getType() {
        return type;
    }

    public Integer getCalories() {
        return calories;
    }
    
}
